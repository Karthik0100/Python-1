def lowercaseAlphabets():

    for c in range(97,123):
        print(chr(c), end = " ");

    print(" ");

def uppercasealphabets():

    for c in range(65,91):
        print(chr(c), end = " ");

    print(" ");

print("\n");
print("Uppercase Alphabets \n");
uppercasealphabets();

print("\n");
print("Lowercase Alphabets \n");
lowercaseAlphabets();

